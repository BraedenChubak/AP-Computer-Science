public class Prog122i {
    public static void main(String[] args) {
        int num = -26;
        double cuberoot = 0;
        int cube = 0;
        while (num < 25) {
            num ++;
            if (num < 0) {
                num *= -1.0;
                cuberoot = Math.pow(num, 1/3.0);
                cuberoot *= -1.0;
                num *= -1.0;
            }
            else {
                cuberoot = Math.pow(num, 1/3.0);
            }
            cube = (int)Math.pow(num, 3);
            System.out.println(num + "\t" + cuberoot + "\t" + cube);
        }
    }
}

